package corej;

public class Eleven2 {
	float one;
	private float two;
	
	public Eleven2() {
		
	}

	public Eleven2(float one, float two) {
		super();
		this.one = one;
		this.two = two;
	}

	public float getOne() {
		return one;
	}

	public void setOne(float one) {
		this.one = one;
	}

	public float getTwo() {
		return two;
	}

	public void setTwo(float two) {
		this.two = two;
	}
	
}
