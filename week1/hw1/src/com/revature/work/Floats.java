package com.revature.work;

public class Floats {
	private float a;
	private float b;
	public Floats(float a, float b) {
		super();
		this.a = a;
		this.b = b;
	}
	public float getA() {
		return a;
	}
	public void setA(float a) {
		this.a = a;
	}
	public float getB() {
		return b;
	}
	public void setB(float b) {
		this.b = b;
	}
	
	
}
